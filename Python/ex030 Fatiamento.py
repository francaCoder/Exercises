"""FATIAMENTO"""

frase = "Curso em Vídeo Python"
print(frase[3]) #Caractere 3
print(frase[3:13]) #Do terceiro caractere até o 12
print(frase[:13]) #Do começo até o 12
print(frase[13:]) #Do 13 até o final
print(frase[1:15:2]) #Do 1 até o 15 pulando de 2 em 2
print(frase[1::2]) #Do primeiro caractere até o último pulando de dois em dois
print(frase[::2]) #Sem início,sem final, pulando de 2 em 2


